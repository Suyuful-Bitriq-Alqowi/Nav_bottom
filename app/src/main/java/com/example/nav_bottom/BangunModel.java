package com.example.nav_bottom;

public class BangunModel {

    String name;
    String img;
    String desc;

    public String getName() {
        return name;
    }

    public String getImg() {
        return img;
    }

    public String getDesc() {
        return desc;
    }

    public BangunModel(String name, String img) {
        this.name = name;
        this.img = img;
        this.desc = desc;
    }
}
